    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        HashMap reporte = userManager.getGenderCount();
        Object[] fill = new Object[4];
        for (Object key : reporte.keySet() ) {
            fill[0] = key;
            fill[1] = reporte.get(key);
            model.addRow(fill);
        }
    }

